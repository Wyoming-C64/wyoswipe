#!/bin/bash

# Script to create all the required autoconf files

echo "**************************************************************"
echo "Running aclocal..."
echo "**************************************************************"
aclocal

echo "**************************************************************"
echo "Generating header..."
echo "**************************************************************"
autoheader

echo "**************************************************************"
echo "Launching automake..."
echo "**************************************************************"
automake --add-missing

echo "**************************************************************"
echo "Finally, auto configure..."
echo "**************************************************************"
autoconf
